package thoiky;

import java.util.ArrayList;

public class ThoiKy {

	private String ten;
	private String thoiGian;
	private String nguoiSangLap;
	private String thuDo;
	private ArrayList<String> lanhDaoQuocGia;
	private String mieuTa;
	private int nguon;

	public void setTen(String ten) {
		this.ten = ten;
	}

	public void setThoiGian(String thoiGian) {
		this.thoiGian = thoiGian;
	}

	public void setNguoiSangLap(String nguoiSangLap) {
		this.nguoiSangLap = nguoiSangLap;
	}

	public void setThuDo(String thuDo) {
		this.thuDo = thuDo;
	}

	public void setLanhDaoQuocGia(ArrayList<String> lanhDaoQuocGia) {
		this.lanhDaoQuocGia = lanhDaoQuocGia;
	}

	public void setMieuTa(String mieuTa) {
		this.mieuTa = mieuTa;
	}

	public void setNguon(int nguon) {
		this.nguon = nguon;
	}

}